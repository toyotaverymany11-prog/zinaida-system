#!/usr/bin/env python3
"""Верификатор поста — автоматическая проверка перед публикацией.
Запуск: python3 scripts/verify-post.py <path/to/post.md>
Возвращает 0 если все проверки пройдены, 1 если есть проблемы."""

import sys
import re

def verify(path):
    with open(path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    issues = []
    warnings = []
    
    # ===== БЛОК 1: СТИЛЬ =====
    # 1. Первый символ — цифра
    first = content.strip()[0] if content.strip() else ''
    if not first.isdigit():
        issues.append("1: Первый символ не цифра/факт")
    
    # 2. Маятник (плюс-минус-плюс)
    if not any(w in content for w in ['Да, ', 'Нет, ', 'Но ', 'Однако ']):
        warnings.append("2: Маятник не обнаружен (нет 'Да/Нет/Но')")
    
    # 7. Честный минус
    if not any(w in content for w in ['обидно', 'сложно', 'больно', 'тяжело', 'трудно',
                                       'унизительно', 'страшно', 'неприятно']):
        warnings.append("7: Честный минус не обнаружен")
    
    # 8. CTA со стрелкой
    if '->' not in content and '➡' not in content and '→' not in content:
        issues.append("8: Нет CTA со стрелкой (->, ➡, →)")
    
    # 9. Open Loop
    if not any(w in content for w in ['Завтра', 'Следующий пост', 'Через день',
                                        'Продолжение', 'Подпишись']):
        warnings.append("9: Open Loop не обнаружен")
    
    # 10. Цифры в лоб
    if not any(c.isdigit() for c in content[:150]):
        issues.append("10: Нет цифр в первых 150 символах")
    
    # 12. Нет соплей
    sopli = ['ты сильная', 'ты справишься', 'всё будет хорошо', 'ты молодец',
             'держись', 'не сдавайся']
    for s in sopli:
        if s in content.lower():
            issues.append(f"12: Запрещённая фраза: «{s}»")
    
    # 15. Нет самореференции
    ai_refs = ['языковая модель', 'мои алгоритмы', 'как ии', 'мои ограничения']
    for r in ai_refs:
        if r in content.lower():
            issues.append(f"15: Самореференция: «{r}»")
    
    # 16. Форматирование
    if '—' in content:  # длинное тире U+2014
        issues.append("16: Есть длинное тире (—)")
    if '•' in content or '●' in content or '◦' in content:
        issues.append("16: Есть жирные точки")
    if '...' in content:
        # проверяем — это многоточие в конце предложения?
        for match in re.finditer(r'\w\.\.\.', content):
            issues.append(f"16: Многоточие после слова: {match.group()}")
            break
    
    # ===== БЛОК 2: ЧЁРНЫЙ СПИСОК =====
    blacklist = {
        'Б1': ['давайте разберёмся', 'важно отметить', 'стоит понимать', 'подводя итог',
               'в заключение', 'как известно'],
        'Б2': ['синергия', 'оптимизировать', 'комплексный подход', 'широкий спектр',
               'на сегодняшний день', 'инновационный', 'уникальный', 'качественный',
               'надёжный'],
        'Б3': ['ты молодец', 'всё будет хорошо', 'ты не одна', 'это нормально',
               'доверься процессу', 'любовь к себе'],
        'Б4': ['более того', 'тем не менее', 'таким образом', 'в свою очередь',
               'следовательно', 'иными словами'],
        'Б5': ['три пункта всегда', 'представь себе', 'pro tip', 'секрет в том что'],
        'Б6': ['может быть', 'возможно', 'в зависимости от ситуации'],
        'Б7': ['коммуникация ключ', 'устанавливай границы', 'работай над собой',
               'он просто не готов', 'отпусти ситуацию'],
        'Б9': ['в этой статье', 'сегодня поговорим о', 'тема нашего разговора',
               'этот пост посвящён', 'вы узнаете из этого'],
        'Б10': ['вы когда-нибудь задумывались', 'хотите узнать секрет',
                'знакомо ли вам', 'признайтесь честно', 'ответ вас шокирует',
                'вы будете удивлены'],
        'Б11': ['своего рода', 'если можно так выразиться', 'весьма вероятно',
                'в определённой степени'],
        'Б12': ['не стоит переживать', 'всё не так страшно', 'нет худа без добра',
                'со временем всё уляжется'],
        'Б13': ['если вы ищете ответ', 'не секрет что', 'данная тема заслуживает',
                'многие задаются вопросом'],
        'Б14': ['как езда на велосипеде', 'мозг компьютер', 'отношения это работа',
                'жизнь это зебра'],
        'Б16': ['в заключение хочется сказать', 'вывод прост', 'берегите себя',
                'в конечном счёте всё зависит от вас'],
        'Б17': ['невероятно', 'фантастический', 'безумно интересный',
                'ошеломительный эффект', 'колоссальная разница'],
        'Б18': ['как я упоминала', 'вот что я предлагаю', 'как языковая модель',
                'из-за ограничений'],
    }
    
    for block, phrases in blacklist.items():
        for phrase in phrases:
            if phrase in content.lower():
                issues.append(f"ЧС {block}: «{phrase}»")
    
    # ===== БЛОК 3: ПЛАТФОРМА =====
    total_len = len(content)
    if total_len < 2000:
        warnings.append(f"Длина: {total_len} (< 2000, маловато для VK)")
    
    # ===== БЛОК 4: ЯЗЫК =====
    # Проверка на английские слова
    eng = ['understand', 'important', 'relationship', 'please', 'sorry']
    for w in eng:
        if w in content.lower():
            warnings.append(f"Английское слово: «{w}»")
    
    return issues, warnings

def main():
    if len(sys.argv) < 2:
        print("Использование: python3 verify-post.py <path/to/post.md>")
        sys.exit(1)
    
    path = sys.argv[1]
    issues, warnings = verify(path)
    
    print(f"\n=== ВЕРИФИКАЦИЯ: {path} ===\n")
    
    if issues:
        print("❌ ПРОБЛЕМЫ:")
        for i in issues:
            print(f"  {i}")
    else:
        print("✅ СТИЛЬ: все проверки пройдены")
    
    if warnings:
        print("⚠️  ЗАМЕЧАНИЯ:")
        for w in warnings:
            print(f"  {w}")
    
    print(f"\n✅ Длина: {len(open(path, encoding='utf-8').read())} знаков")
    print()
    
    if issues:
        print("❌ ИТОГ: исправь проблемы и запусти заново")
        sys.exit(1)
    else:
        print("✅ ИТОГ: можно публиковать")
        sys.exit(0)

if __name__ == '__main__':
    main()
